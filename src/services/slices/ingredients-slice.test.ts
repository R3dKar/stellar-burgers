describe('ingredientsSlice', () => {
  it('should init', () => {
    const state = ingredientsReducer(undefined, { type: 'UNKNOWN_ACTION' });

    expect(state).toEqual(initialState);
  });

  it('handles successful updating empty state', () => {
    const state = ingredientsReducer(initialState, {
      type: ingredientsRetrieve.fulfilled.type,
      payload: ingredients1
    });

    expect(state.ingredients).toEqual(ingredients1);
  });

  it('handles successful updating filled state', () => {
    const state = ingredientsReducer(
      { ingredients: ingredients1 },
      { type: ingredientsRetrieve.fulfilled.type, payload: ingredients2 }
    );

    expect(state.ingredients).toEqual(ingredients2);
  });
});
